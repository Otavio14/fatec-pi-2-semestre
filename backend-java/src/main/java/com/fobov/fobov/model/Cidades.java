package com.fobov.fobov.model;

public class Cidades {
    private int id_cidade;
    private String nome;

    // ID
    public int getId() {
        return id_cidade;
    }

    public void setId(int id_cidade) {
        this.id_cidade = id_cidade;
    }

    // NOME
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}